    private void setTableData(List<User> users) {
        for(User user : users) {
            defaultTableModel.addRow(new Object[]{user.getId(), user.getName(), user.getPhoneNumber()
                                    , user.getUserName(), user.getPassword(), user.getRole()
                                    , user.getFavorite(), user.getAbout()});
        }
    }

